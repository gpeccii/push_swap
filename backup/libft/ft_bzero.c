#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	i;
	char	*tmp;

	i = 0;
	tmp = (char *) s;
	if (n == 0)
		return ;
	while (i < n)
	{
		tmp[i] = 0;
		i++;
	}
	return ;
}
